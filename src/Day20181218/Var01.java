package Day20181218;

public class Var01 {
    public static void main(String[] args) {
        // 자바에서 다루는 기본 자료형
        System.out.println("안녕 자바"); // 문자열 String
        // String 은 기본 자료형이 아니며 Java 라이브러리 내 문자열 처리 클래스인 String 클래스를 사용한다
        System.out.println(7); // 정수 Int
        System.out.println(9.8); // 실수 float, double
        System.out.println('가'); // 하나의 문자 char
        System.out.println(true); // boolean 참
        System.out.println(false); // boolean 거짓
    }
}
